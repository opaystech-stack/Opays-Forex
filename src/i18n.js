export function useT() {
  const { language } = useApp();
  return (key) => {
    const lang = language || 'fr';
    const parts = key.split('.');
    let cur = translations[lang];
    for (const p of parts) {
      if (!cur) return key;
      cur = cur[p];
    }
    return cur || key;
  };
}

export default translations;
